export function t(key, lang = "en") {
  const entry = STRINGS[key];
  if (!entry) return key;
  return entry[lang] || entry.en || key;
}

// Pick a language-aware value with fallback to English / base.
// e.g. pickLang(profile.intro, profile.intro_id, "en") -> uses id if non-empty, else en.
export function pickLang(en, alt, lang) {
  if (lang === "id" && alt && String(alt).trim().length > 0) return alt;
  return en;
}
